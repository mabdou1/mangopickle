	      if (n % 2 != 0) { // if n is odd
	         System.out.println("Weird");
	      } else { // if n is even
	         if (n >= 2 && n <= 5)  // inclusive range of 2 to 5
	            System.out.println("Not Weird");
	         else if (n <= 20)  // inclusive range of 6 to 20
	            System.out.println("Weird");
	         else  // greater than 20
	            System.out.println("Not Weird");
	      }
	      
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

