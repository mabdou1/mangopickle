package Day_11_ReviewSession;

public class WhileLoopWithScanner {

	public static void main(String[] args) {
	
		/*
	      - Write program to print all alphabets with their ASCII values. (lower / uppercase)
	       */
	      char a = 'a';
	      while (a <= 'z') {
	         int asciiValue = a;
	         System.out.println(a+" "+asciiValue);
	         a++;
	      }
	      char A = 'A';
	      while (A <= 'Z') {
	         int asciiValue = A;
	         System.out.println(A+" "+asciiValue);
	         A++;
	      } 
		
		
		
		
		

	}

}
