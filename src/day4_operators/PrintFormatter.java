package day4_operators;

public class PrintFormatter {

	public static void main(String[] args) {
		
		
		
		System.out.printf("Hello My name is %S", " Mamata");
		
		
		
		System.out.println();
		
		System.out.printf("%s My name is %s", "Bonjour","Mamata" );
		
		System.out.println();
		
		
		System.out.printf("Is sunny today? %b",false);
		
		
		
		

	}

}
