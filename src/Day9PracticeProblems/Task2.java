package Day9PracticeProblems;

public class Task2 {

	public static void main(String[] args) {
		// Write a program to calculate the sum of all the numbers between 1 and 100
		
		
		int sum = 0;
		for (int i = 1; i <= 100; i++) {
			sum += i; 
		}
		System.out.println("sum: " + sum);
		
		

		
		
		
		
		
		
		

	}

}
